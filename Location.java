public class Location {
    String name; 
    String description; 
    boolean hasItem; 

    Location(String n, String d, boolean hI){
        this.name=n; 
        this.description=d; 
        this.hasItem= hI; 
    }

    public String getName(){
        return this.getName(); 
    }

    public String getDescription(){
        return this.getDescription(); 
    }
    
    public boolean getHasItem(){
        return this.getHasItem();
    }
}
